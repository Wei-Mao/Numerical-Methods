function [p,q]=harvey_chanshang%(x,y)
%======差商求法=======
%用一个n X (n+1)矩阵P存储有关，第一列为插值节点，第二列为零阶差商（函数值），
%后续各列依次递推。
%测试用例
x=[10 11 12 13  14]';
y=log(x);
%=====================
n=length(x);
p(:,1)=x;
p(:,2)=y;
for j=1:n-1
    p(1:n-j,j+2)=diff(p(1:n-j+1,j+1))./(x(j+1:n)-x(1:n-j));
end
q=p(1,2:n+1)';
end